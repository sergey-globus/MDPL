.386 
EXTRN TOUHEX : NEAR
EXTRN TOSBIN : NEAR
EXTRN UHEX : BYTE
EXTRN SBIN : BYTE
EXTRN SIGN : BYTE

PUBLIC NEWLINE
PUBLIC OUTUHEX
PUBLIC OUTSBIN

DATASEG SEGMENT USE16 PARA PUBLIC 'DATA'
	
    OUTUHEXMSG DB 'Unsigned hexadecimal number: $'
    OUTSBINMSG DB 'Signed binary number: $'
DATASEG ENDS

CODESEG SEGMENT use16 PARA PUBLIC 'CODE'
    ASSUME CS:CODESEG

NEWLINE PROC NEAR
    MOV AH, 2
    MOV DL, 13
    INT 21H
    MOV DL, 10
    INT 21H
    RET
NEWLINE ENDP

OUTUHEX PROC NEAR
    MOV AH, 9
    MOV DX, OFFSET OUTUHEXMSG
    INT 21H

    CALL TOUHEX

    MOV AH, 9
    MOV DX, OFFSET UHEX
    INT 21H

    CALL NEWLINE
    RET
OUTUHEX ENDP

OUTSBIN PROC NEAR
    MOV AH, 9
    MOV DX, OFFSET OUTSBINMSG
    INT 21H

    CALL TOSBIN

    MOV AH, 2
    MOV DL, SIGN
    INT 21H

    MOV AH, 9
    MOV DX, OFFSET SBIN
    INT 21H

    CALL NEWLINE
    RET
OUTSBIN ENDP

CODESEG ENDS
END
